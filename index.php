     <center><h1>Agriculture</h1></center>
     <p style="padding:1rem 2rem"> Food is a basic requirement for every living being. We depend on plant and animals for food. Ancient men began the cultivation of food in a small area and used certain procedures for their management and improvement. This art of cultivation of the crop is called agriculture.

In agriculture, there are certain parameters to be considered such as the type of crop, properties of soil, climate etc. Depending upon these parameters, farmers decide which crop is to be cultivated at what time of the year and place. Moreover, to yield a high-quality product, suitable soil, climate and season are not sufficient. It requires a set of procedures which needed to be followed. The measures which are followed to raise crops are called agricultural practices. Different agricultural practices are discussed below.</P>
   
      <center><img src=".\agri\img11.png"></center>
      <center><h2> SOIL PEREPRATION </h2></center>
      <center><img src=".\agri\soil.jpg"></center>
        <P style="padding:1rem 2rem">Before raising a crop, the soil in which it is to be grown is prepared by ploughing, levelling, and manuring. Ploughing is the process of loosening and digging of soil using a plough. This helps in proper aeration of the soil. After ploughing, the soil is distributed evenly and levelled in the process called levelling. The soil is then manured.</P>
        <center><h2>SOWING</h2></center>
        <center><img src=".\agri\img13.jpg"></center>
        <p style="padding:1rem 2rem">Selection of seeds of good quality crop strains is the primary stage of sowing. After the preparation of soil, these seeds are dispersed in the field and this is called sowing. Sowing can be done manually, by hand or by using seed drilling machines. Some crops like paddy are first grown into seedlings in a small area and then transplanted to the main field.</P>
        <center><h2>MANURING</h2></center>
        <center><img src=".\agri\img14.jpg"></center>
        <p style="padding:1ressm 2rem">Crops need nutrients to grow and produce yield. Thus, the supply of nutrients at regular intervals is necessary. Manuring is the step where nutritional supplements are provided and these supplements may be natural (manure) or chemical compounds (fertilizers). Manure is the decomposition product of plant and animal wastes. Fertilizers are chemical compounds consisting of plant nutrients and are produced commercially. Apart from providing nutrients to crop, manure replenishes soil fertility as well. Other methods for soil replenishment are vermicompost, crop rotation, planting of leguminous plants.</p>
        <center><h2>IRRIGATION</h2></center>
        <center><img src=".\agri\immm.jpg"></center>
        <p style="padding:1rem 2rem">Irrigation is the supply of water. Sources of water can be wells, ponds, lakes, canals, dams etc. Over irrigation may lead to waterlogging and damage the crop. This frequency and interval between successive irrigation need to be controlled.</p>
        <center><h2>WEEDING</h2></center>
        <center><img src=".\agri\weeding.jpg"></center>
        <p style="padding:1rem 2rem">Weeds are unwanted plants which grow among crops. They are removed by using weedicides, by manually pulling them with hands and some are removed during soil preparation.</p>
        <center><h2>HARVESTING</h2></center>
        <center><img src=".\agri\img16.jpg"></center>
        <p style="padding:1rem 2rem">Once the crop is matured, it is cut and gathered, this process is called harvesting. Followed by harvesting, grains are separated from the chaff either by threshing, or manually in small scale (winnowing).</p>
        <center><h2>STORAGE</h2></center>
        <center><img src=".\agri\imd17.jpg"></center>
        <p style="padding:1rem 2rem">Grains yielded are stored in granaries or bins at godowns for later use or marketing. Therefore, methods of crop protection need to be better. In order to protect grains from pest and rodents- cleaning, drying, fumigation, etc., are done prior to storing.</p>


